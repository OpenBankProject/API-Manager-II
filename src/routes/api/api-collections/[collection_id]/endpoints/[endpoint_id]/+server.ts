import { json } from "@sveltejs/kit";
import type { RequestHandler } from "./$types";
import { obp_requests } from "$lib/obp/requests";
import { SessionOAuthHelper } from "$lib/oauth/sessionHelper";
import { createLogger } from "$lib/utils/logger";

const logger = createLogger("ApiCollectionEndpointAPI");

export const DELETE: RequestHandler = async ({ params, locals }) => {
  const session = locals.session;

  if (!session?.data?.user) {
    return json({ error: "Unauthorized" }, { status: 401 });
  }

  const { collection_id, endpoint_id } = params;

  if (!collection_id) {
    return json({ error: "collection_id is required" }, { status: 400 });
  }

  if (!endpoint_id) {
    return json({ error: "endpoint_id is required" }, { status: 400 });
  }

  // Get the OAuth session data
  const sessionOAuth = SessionOAuthHelper.getSessionOAuth(session);
  const accessToken = sessionOAuth?.accessToken;

  if (!accessToken) {
    logger.warn(
      "No access token available for removing endpoint from collection",
    );
    return json({ error: "No API access token available" }, { status: 401 });
  }

  try {
    logger.info("=== REMOVE ENDPOINT FROM API COLLECTION ===");
    logger.info(`Collection ID: ${collection_id}`);
    logger.info(`Endpoint ID: ${endpoint_id}`);

    const endpoint = `/obp/v6.0.0/my/api-collection-ids/${collection_id}/api-collection-endpoint-ids/${endpoint_id}`;
    logger.info(`DELETE ${endpoint}`);

    const response = await obp_requests.delete(endpoint, accessToken);

    logger.info("Endpoint removed from API collection successfully");
    logger.info(`Response: ${JSON.stringify(response)}`);

    return json(response);
  } catch (err) {
    logger.error("Error removing endpoint from API collection:", err);

    let errorMessage = "Failed to remove endpoint from API collection";
    let obpErrorCode = undefined;

    if (err instanceof Error) {
      errorMessage = err.message;
      if ("obpErrorCode" in err) {
        obpErrorCode = (err as any).obpErrorCode;
      }
    }

    const errorResponse: any = { error: errorMessage };
    if (obpErrorCode) {
      errorResponse.obpErrorCode = obpErrorCode;
    }

    return json(errorResponse, { status: 500 });
  }
};
